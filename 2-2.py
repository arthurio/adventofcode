class Submarine:
    class Move(str, Enum):
        forward = "forward"
        up = "up"
        down = "down"

    aim: int = 0
    horizontal_position: int = 0
    depth: int = 0

    def move(self, command: Move, units: int):
        match command:
            case Submarine.Move.forward:
                self.horizontal_position += units
                self.depth += self.aim * units
            case Submarine.Move.up:
                self.aim -= units
            case Submarine.Move.down:
                self.aim += units

        return self

    def __str__(self):
        return (
            "Submarine("
            f"aim={self.aim}, "
            f"horizontal={self.horizontal_position}, "
            f"depth={self.depth}, "
            f"total={self.depth * self.horizontal_position}"
            ")"
        )


